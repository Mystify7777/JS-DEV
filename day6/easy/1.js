//Iterate 0 to 10 using for loop, do the same using while and do while loop
console.log("Using for loop:");
for (let i = 0; i <= 10; i++) {
    console.log(i);
}
console.log("Using while loop:");
let j = 0;
while (j <= 10) {
    console.log(j);
    j++;
}
console.log("Using do-while loop:");
let k = 0;
do {
    console.log(k);
    k++;
} while (k <= 10);
