//Iterate 10 to 0 using for loop, do the same using while and do while loop
console.log("Using for loop:");
for (let i = 10; i >= 0; i--) {
    console.log(i);
}
console.log("Using while loop:");
let j = 10;
while (j >= 0) {
    console.log(j);
    j--;
}
console.log("Using do-while loop:");
let k = 10;
do {
    console.log(k);
    k--;
} while (k >= 0);
